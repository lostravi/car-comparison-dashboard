// Utility Functions
export const getMinMaxPrice = () => {
  const prices = cars.map((car) => car.price);
  return [Math.min(...prices), Math.max(...prices)];
};

export const getMinMaxYear = () => {
  const years = cars.map((car) => car.year);
  return [Math.min(...years), Math.max(...years)];
};

export const getAllBrands = () => {
  return [...new Set(cars.map((car) => car.brand))].sort();
};

export const getAllBodyTypes = () => {
  return [...new Set(cars.map((car) => car.bodyType))].sort();
};

export const getAllFuelTypes = () => {
  return [...new Set(cars.map((car) => car.fuelType))].sort();
};
